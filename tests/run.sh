#!/bin/bash

set -euo pipefail


#======================================================================================================================
# Welcome message.
#======================================================================================================================

echo "Welcome to the test suite."


#======================================================================================================================
# Welcome message.
#======================================================================================================================

export SHUNIT2=${GITHUB_WORKSPACE}/shunit2/shunit2
chmod +x ${SHUNIT2}


#======================================================================================================================
# Load all tests.
#======================================================================================================================

for FILE in $(find . -type f -name "test-*.sh") ; do
    echo "Running tests in ${FILE}."
    chmod +x ${FILE} && ${FILE}
done


#======================================================================================================================
# End message.
#======================================================================================================================

echo "Testing complete."
