#!/bin/bash

set -euo pipefail


#======================================================================================================================
# Welcome message.
#======================================================================================================================

echo "Welcome to the test suite."


#======================================================================================================================
# Welcome message.
#======================================================================================================================

export SHUNIT2=${GITHUB_WORKSPACE}/tests/shunit2


#======================================================================================================================
# Load all tests.
#======================================================================================================================

for FILE in $(find . -type f -name "test-*.sh") ; do
    echo "Running tests in ${FILE}."
    ${FILE}
done


#======================================================================================================================
# End message.
#======================================================================================================================

echo "Testing complete."
