#!/bin/withenv sh


#======================================================================================================================
# Run preflight checks
#======================================================================================================================

MOD=${1}
nu -c "use bf-${MOD} run ; run preflight"


#======================================================================================================================
# Exit shell gracefully if the preflight has exited early.
#======================================================================================================================

if bf-env-check --reload ${MOD}_PREFLIGHT_EARLY_EXIT ; then exit 0; fi
